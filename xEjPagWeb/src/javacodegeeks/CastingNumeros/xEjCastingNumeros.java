package javacodegeeks.CastingNumeros;

/**
 * @see @since @version @author Raul Vela Salas
 */
public class xEjCastingNumeros {

 public static void main(String[] args) {

  byte b = 10;
  b *= 5.7;
  System.out.println(b);

  System.out.println("");

  byte b1 = 100;
  b1 /= 2.5;
  System.out.println(b1);

  System.out.println("");

  char ch = '0';
  ch *= 1.1;
  System.out.println(ch);

  System.out.println("");

  char ch1 = 'A';
  ch1 *= 1.5;
  System.out.println(ch1);

 }
}
