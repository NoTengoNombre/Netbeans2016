public class VademecumPag21IncrementoDecremento {

 public static void main(String[] args) {

  int x0 = 0;
  System.out.println(x0++);

  int x1 = 0;
  System.out.println(++x1);

  int x2 = 0;
  System.out.println(x2--);

  int x3 = 0;
  System.out.println(--x3);

 }
}
